# 5-1. Conditional Tests

car = 'subaru'
print("Is car == 'subaru'? I predict True")
print(car == 'subaru')

print("\nIs car == 'SuBaRu'? I predict True")
print(car.lower() == 'subaru')

print("\nIs car == 'ferrari'? I predict False")
print(car == 'ferrari')

print("\nIs car != 'audi'? I predict True")
print(car != 'audi')
